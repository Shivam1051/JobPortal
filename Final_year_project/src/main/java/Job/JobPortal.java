package Job;

public class JobPortal {
	private String Username;
	private int Password;
	public JobPortal() {
		
	}
	public String getUsername() {
		return Username;
	}
	public void setUsername(String username) {
		Username = username;
	}
	public int getPassword() {
		return Password;
	}
	public void setPassword(int password) {
		Password = password;
	}

}
